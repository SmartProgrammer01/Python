# Datatypes in python

# Simply assigning the values

x = "Pranav"                                    # String
y = 21                                          # Integer
z = 32.4                                        # Float
a = 2j                                          # Complex
b = ["apple", "banana", "cherry"]	            # List	
c = ("apple", "banana", "cherry")		        # Tuple
d = range(6)		                            # Range
e = {"name" : "John", "age" : 36}		        # Dictionary
f = {"apple", "banana", "cherry"}		        # Set
g = frozenset({"apple", "banana", "cherry"})	# Frozen set	
h = True		                                # Boolean
i = b"Hello"		                            # Bytes
j = bytearray(5)		                        # Bytearray
k = memoryview(bytes(5))                        # Memoryview


print(x)
print(y)
print(z)
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)
print(j)
print(k,"\n")

